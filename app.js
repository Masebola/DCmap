function migrateGranularProgress() {
  splitChildrenByLegacyId.forEach((children, legacyId) => {
    if (!(legacyId in state.progress)) return;
    const alreadyHasChildren = children.some(child => child.id in state.progress);
    if (!alreadyHasChildren) {
      let remaining = Math.max(0, Number(state.progress[legacyId] || 0));
      children.forEach(child => {
        const total = estimateIssueCount(child);
        const completed = Math.min(total, remaining);
        state.progress[child.id] = completed;
        remaining -= completed;
      });
    }
    delete state.progress[legacyId];
  });
}

migrateLegacyAliases();
migrateGranularProgress();

function estimateIssueCount(item) {
  if (Number.isFinite(item.count)) return Math.max(0, item.count);
  const text = item.t || "";
  const matches = [...text.matchAll(/#\s*(\d+)(?:\s*[–—-]\s*(\d+|\+))?/g)];
  if (matches.length) {
    return matches.reduce((sum, match) => {
      const start = Number(match[1]);
      const end = match[2] && match[2] !== "+" ? Number(match[2]) : start;
      return sum + Math.max(1, Math.abs(end - start) + 1);
    }, 0);
  }
  const named = text.match(/(\d+)\s+one[- ]shots?/i);
  if (named) return Number(named[1]);
  return 1;
}
