function issueReferenceCount(text) {
  return (String(text || "").match(/#\s*\d+/g) || []).length;
}

function cleanSplitTitle(text) {
  return String(text || "")
    .replace(/^\s*(?:tie-ins?|selected tie-ins?|ongoing tie-ins?)\s*:\s*/i, "")
    .replace(/^\s*(?:Murderer\?|Fugitive|Trial of the Amazons)\s*:\s*/i, "")
    .replace(/^\s*[()]+|[()]+\s*$/g, "")
    .replace(/[.;]\s*$/, "")
    .trim();
}

