function totals(ids) {
  const total = ids.reduce((n,id)=>n+itemTotal(id),0);
  const read = ids.reduce((n,id)=>n+itemRead(id),0);
  const validIds = ids.filter(id=>itemTotal(id)>0);
  const groups = validIds.filter(id=>itemComplete(id)).length;
  return { total, read, groups, groupTotal: validIds.length, pct: total ? Math.round(read/total*100) : 0 };
}
